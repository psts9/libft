#include "libft.h"

int	ft_isalpha(int c)
{
	unsigned char u_char;

	u_char = M_UCHAR(c);
	if (c >= 'A' && c <= 'Z')
		return (1);
	if (c >= 'a' && c <= 'z')
		return (1);
	return (0);
}
